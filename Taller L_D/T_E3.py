# Dado el siguiente arreglo [ “Colombia”, “es”, “un”, “país”, “extraordinario” ], imprima en consola el
# mensaje “Colombia es un país extraordinario” usando el método join
# join se utiliza para convertir una lista en una cadena, separada por un caracter deseado
mensaje = [ "Colombia", "es", "un", "país","extraordinario" ]

mensaje_join = "@".join(mensaje)
print(mensaje_join)

#Colombia@es@un@país@extraordinario

